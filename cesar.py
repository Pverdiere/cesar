def chiffrement(msg:str,key:int) -> str:
    new_msg = ""
    for char in msg:
        new_msg += chr(ord(char) + key)
    return new_msg

def dechiffrement(msg:str,key:int) -> str:
    new_msg = ""
    for char in msg:
        #print(char)
        new_msg += chr(ord(char) - key)
    return new_msg

